class Employee
{
   constructor(name,age,salary,dep)
   {
     this.name = name
     this.age = age 
     this.salary = salary 
     this.dep = dep
   } 
   show()
   {
     console.log("\nName  : " , this.name)
     console.log("Age  : " , this.age)
     console.log("Salary  : " , this.salary)
     console.log("Department  : " , this.dep)
   }
}

